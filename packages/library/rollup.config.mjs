import resolve from '@rollup/plugin-node-resolve';
import babel from "@rollup/plugin-babel";
import typescript from 'rollup-plugin-typescript2'
import postcss from 'rollup-plugin-postcss'
import postcssModules from 'postcss-modules'
import commonjs from '@rollup/plugin-commonjs'
import peerDepsExternal from 'rollup-plugin-peer-deps-external';
import path from 'path'

export default {
  input: {
    index: path.join('./src', 'index')
  },
  // output: ['cjs', 'esm'].map(getOutputOption),
  output: ['esm'].map(getOutputOption),
  external: [/@babel\/runtime/, /.yarn/],
  plugins: [
    commonjs(),
    resolve({ extensions: ['.js', '.jsx', '.ts', '.tsx'] }), 
    typescript({
      include: ['./src/**/*.{ts,tsx}'],
      exclude: [
        'node_modules',
        'rollup.config.mjs',
        '**/stories',
        '**/*.stories.{ts,tsx}'
      ],
      tsconfigOverride: {
        compilerOptions: {
          noEmit: false,
          emitDeclarationOnly: true,
          mapRoot: process.cwd(),
          declarationDir: path.join('./dist'),
        },
      },
      useTsconfigDeclarationDir: true,
    }),
    babel({
      babelHelpers: "runtime",
      exclude: ['.yarn/**'],
      presets: [
        "@babel/preset-env",
        "@babel/preset-react",
        "@babel/preset-typescript",
      ],
      extensions: ['.js', '.jsx', '.ts', '.tsx'],
    }),
    postcss({
      config: {
        path: './postcss.config.js'
      },
      extensions: ['.css'],
    }),
    peerDepsExternal(),
    {
      name: 'customRollupPlugin`',
      generateBundle: (options, bundle) => {
        Object.entries(bundle).forEach(entry => {

          // early return if the file we're currently looking at doesn't need to be acted upon by this plugin
          if (!entry[0].match(/.*(.css.esm.js)$/)) {
            return;
          }

          // this line only runs for .scss.js files, which were generated by the postcss plugin.
          // depending on the use-case, the relative path to style-inject might need to change
          bundle[entry[0]].code = entry[1].code.replace(
            '/Users/eunsonny/Desktop/project/ES-UI-library/.yarn/cache/style-inject-npm-0.3.0-e2042d3aac-fa5f5f6730.zip/node_modules/style-inject/dist/style-inject.es.js',
            'style-inject',
          );
        });
      },
    }
  ],
};

/** @type {() => import('rollup').OutputOptions} */
function getOutputOption(format) {
  return {
    dir: './dist',
    sourcemap: true,
    format,
    entryFileNames: `[name].${format}.js`,
    interop: 'auto',
    preserveModules: true,
    preserveModulesRoot: './src',
  };
}
